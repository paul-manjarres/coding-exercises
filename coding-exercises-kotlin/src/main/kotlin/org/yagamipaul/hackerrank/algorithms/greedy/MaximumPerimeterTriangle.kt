package org.yagamipaul.hackerrank.algorithms.greedy

import java.util.*

/**
 * Maximum perimeter triangle
 * Jean Paul Manjarres Correal
 */

fun main(args: Array<String>) {
    val scanner = Scanner(System.`in`)

    val n = scanner.nextInt()
    val l = IntArray(n)
    for (i in 0.until(n)) {
        l[i] = scanner.nextInt()
    }



    scanner.close()

}